package com.drizzle.drizzledaily.utils;

import android.app.Activity;

import com.drizzle.drizzledaily.R;

/**
 * 处理activity换肤
 */
public class ThemeUtils {
    private String[] strings=new String[]{"原装色","火焰红","冷酷蓝","高级黑","热烈橙","生命绿","高贵紫","香蕉黄"};

    public static void onActivityCreateSetTheme(Activity activity,int themeid) {
        switch (themeid) {
            case 0:
                activity.setTheme(R.style.TealAppTheme);
                break;
            case 1:
                activity.setTheme(R.style.RedAppTheme);
                break;
            case 2:
                activity.setTheme(R.style.BlueAppTheme);
                break;
            case 3:
                activity.setTheme(R.style.BlackAppTheme);
                break;
            case 4:
                activity.setTheme(R.style.OrangeAppTheme);
                break;
            case 5:
                activity.setTheme(R.style.GreenAppTheme);
                break;
            case 6:
                activity.setTheme(R.style.PurpleAppTheme);
                break;
            case 7:
                activity.setTheme(R.style.YellowAppTheme);
                break;
            default:
                break;
        }
    }
}
